package com.biz.num;

// 지금부터 자바 클래스가 시작된다. 라는 선언
// 이 선언문이 문법에 맞도록 작성되었으면
// javac는 컴파일 과정을 수행해서
// Integer01.class 라는 byte 파일을 생성한다.
public class Integer01 {
	
	// 지금부터 여기 나열된 명령문들을 실행하라
	// 라는 선언문
	public static void main(String[] args) {
		System.out.println(); // 빈줄을 한줄 띄어라
		System.out.println("Hello"); // Hello라는 글자를 보여라
		System.out.println(30+40); // 30 + 40 한 덧셈 결과를 보여라
		System.out.println(1000);
		System.out.println(2147483648l + 1);
		System.out.println(2147483648.0);
		System.out.println(2147483648392939293922293929.0);
	}

}
